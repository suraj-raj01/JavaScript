/*

Bitwise Operators.
a) & --> AND 
b) | --> OR
c) ^ --> XOR
d) ~ --> NEGATION
e) << --> LEFT SHIFT
f) >> --> RIGHT SHIFT

*/

let a,b;
a=10
b=15;

document.write(a&b);
console.log(a&b);

document.write(a|b);
console.log(a|b);

document.write(a^b);
console.log(a^b);

document.write(a<<b);
console.log(a<<b);

document.write(~a);
console.log(~a);

document.write(a>>b);
console.log(a>>b);